import mongoose from "mongoose";
// JUST FOR EXAMPLE
const postSchema = mongoose.Schema({
  title: String,
  message: String,
  name: String,
  creator: String,
  tags: [String],
  selectedFile: String,
  likes: {
    type: [String],
    default: [],
  },
  createdAt: {
    type: Date,
    default: new Date(),
  },
});

const DataModel = mongoose.model("DataModel", postSchema);

export default DataModel;
